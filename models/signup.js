const { string } = require("joi");
const mongoose = require("mongoose");
const loginSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  dateOfBirth: {
    type: String,
    required: true,
  },

  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: String,
    required: true,
  },
  resetLink: {
    data: String,
    default: "",
  },
});

module.exports = mongoose.model("signup", loginSchema);
